  # Seeds::LoadHomesteadsCoords.load_coords 
  def self.load_coords
    ok = 0
    no = 0
    self.parsed_datas.each do |coords|
      if self.set_coords(coords) 
        ok += 1 
      else  
        no += 1 
      end  
    end
    puts "\n End  ok=#{ok}  no=#{no} \n"
  end

  def self.set_coords(coords)
    home = Homestead.where(:domain_id => coords[1].to_i).where(:site_num => coords[0].to_i).first
    if home
      home.coords = coords[2].to_s.chomp.gsub(/\s*\z/, '')
      if home.save 
        return true
      else
        return false
      end  
    else
      return false
    end
  end 

