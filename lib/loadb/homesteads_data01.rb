  def self.save
    nn = 0
    self.data_parser.each do |arr|
     homs = Homestead.where(:domain_id => 3).where(:site_num => arr[1])  
     if homs.size == 1
       hom = homs.first
       if hom.square_meters == arr[2] and hom.price == arr[3]
         hom.price_infra = arr[4]
         nn += 1 if hom.save
       else
         puts "sm=#{hom.square_meters}  arr2=#{arr[2]}  |  pr=#{hom.price}  arr3=#{arr[3]}"
       end
     end
    end
    puts "Ok  size=#{self.data_parser.size}  nn=#{nn}"
  end


